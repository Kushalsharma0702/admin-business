export function formatEntry(row: Record<string, unknown>) {
  return {
    id: row.id,
    periodLabel: row.period_label,
    periodStart: row.period_start,
    periodEnd: row.period_end,
    status: row.status,
    employeeIds: safeJson(row.employee_ids as string, []),
    totalAmount: row.total_amount ?? null,
    notes: row.notes ?? "",
    documentPaths: safeJson(row.document_paths as string, []),
    metadata: safeJson(row.metadata as string, { employeeRows: [] }),
    isAutoGenerated: row.is_auto_generated === 1,
    createdAt: row.created_at,
    updatedAt: row.updated_at,
    submittedAt: row.submitted_at ?? null,
  };
}

export function safeJson<T>(raw: string | null | undefined, fallback: T): T {
  if (!raw) return fallback;
  try {
    return JSON.parse(raw) as T;
  } catch {
    return fallback;
  }
}

export function nowIso() {
  return new Date().toISOString();
}

export function genId(prefix: string) {
  return `${prefix}_${Date.now()}_${Math.random().toString(36).slice(2, 7)}`;
}

/** Simple password hash (SHA-256 via Web Crypto, for testing only) */
export async function hashPassword(plain: string): Promise<string> {
  const enc = new TextEncoder().encode(plain);
  const buf = await crypto.subtle.digest("SHA-256", enc);
  return Array.from(new Uint8Array(buf))
    .map((b) => b.toString(16).padStart(2, "0"))
    .join("");
}

export async function verifyPassword(plain: string, hash: string): Promise<boolean> {
  return (await hashPassword(plain)) === hash;
}
