/**
 * Shared response helpers — consistent envelope for all endpoints.
 */

export function ok(data: unknown, message = "Success", extra?: object) {
  return { success: true, message, data, ...extra };
}

export function created(data: unknown, message = "Created") {
  return { success: true, message, data };
}

export function fail(message: string, errors?: { field: string; message: string }[]) {
  return { success: false, message, ...(errors ? { errors } : {}) };
}

export function paged(
  data: unknown[],
  message: string,
  page: number,
  per_page: number,
  total_items: number,
) {
  return {
    success: true,
    message,
    data,
    pagination: {
      page,
      per_page,
      total_items,
      total_pages: Math.ceil(total_items / per_page),
    },
  };
}

/** Build camelCase task object from DB row */
export function formatTask(row: Record<string, unknown>) {
  return {
    id: row.id,
    clientId: row.client_id,
    assignedBy: row.assigned_by,
    title: row.title,
    description: row.description,
    status: row.status,
    adminStatus: row.admin_status,
    taskType: row.task_type,
    metadata: safeJson(row.metadata as string, {}),
    completionNote: row.completion_note ?? null,
    completedAt: row.completed_at ?? null,
    createdAt: row.created_at,
    updatedAt: row.updated_at,
  };
}

